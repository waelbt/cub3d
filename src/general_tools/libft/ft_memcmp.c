#include "../../includes/libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t			i;
	unsigned char	*str;
	unsigned char	*ptr;

	i = 0;
	str = (unsigned char *) s1;
	ptr = (unsigned char *) s2;
	if (n == 0)
		return (0);
	while (ptr[i] == str[i] && i < n - 1)
	{
		i++;
	}
	return (str[i] - ptr[i]);
}
