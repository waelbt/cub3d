#include "../../includes/libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t			i;
	unsigned char	*p;
	unsigned char	*s;

	p = (unsigned char *)dst;
	s = (unsigned char *)src;
	i = 0;
	if (!p && !s)
		return (0);
	while (i < n)
	{
		*p = *s;
		i++;
		p++;
		s++;
	}
	return (dst);
}
